    public function updateContactInfo(Request $request)
    {
        
        $key = Setting::where('key', 'contact_info')->first();
        $value = $request->value;
        $value = json_encode($value);
        $key->update(
            [
                'value' => $value,
            ]
        );

        return response()->json(['key' => $key], 200);
    }

